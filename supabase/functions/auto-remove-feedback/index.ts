import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.58.0';

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

Deno.serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    const supabaseClient = createClient(
      'https://bgqlhaqwsnfhhatxhtfx.supabase.co',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? '',
      {
        auth: {
          autoRefreshToken: false,
          persistSession: false,
        },
      }
    );

    console.log('Running auto-removal of old feedback posts...');

    // Call the database function to auto-remove old posts
    const { error } = await supabaseClient.rpc('auto_remove_old_feedback_posts');

    if (error) {
      console.error('Error auto-removing feedback posts:', error);
      throw error;
    }

    console.log('Successfully auto-removed old feedback posts');

    return new Response(
      JSON.stringify({ 
        success: true, 
        message: 'Auto-removal completed successfully' 
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      }
    );
  } catch (error) {
    console.error('Error in auto-remove-feedback function:', error);
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500,
      }
    );
  }
});
